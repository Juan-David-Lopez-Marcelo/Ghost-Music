package Modelo;

/**
 *
 * @author Usuario
 */
public class Proveedor extends Model{
    
    private String id;
    private String correo;

    public Proveedor(String nombre, String correo, String id) {
        super(nombre);
        this.correo = correo;
        this.id = id;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
    
}
